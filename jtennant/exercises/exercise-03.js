// Create function named 'min' that takes two parameters and displays the
// parameter with the smallest value.

function min(a, b) {
    if (a < b) {
        console.log(a);
    } else {
        console.log(b);
    }
}

min(1,0); // should display 0
min(0,1); // should display 0
min(3,1); // should display 1
min(3,1); // should display 1
